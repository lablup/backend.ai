class TestNetworkLinkCollector:
    async def test_collects_our_devices_and_ignores_the_hosts(self) -> None:
        node = FakeNode("n1", {("ip", "-json", "link", "show"): IP_LINK_JSON})
        found = await NetworkLinkCollector(node).collect()
        assert {r.ident for r in found} == {
            "baibr4098",
            "baivx4098",
            "bailo4098",
            "bai7a702208f38",
            "veth5cd2afe",
        }

    async def test_docker_veth_on_a_docker_bridge_is_not_ours(self) -> None:
        node = FakeNode("n1", {("ip", "-json", "link", "show"): IP_LINK_JSON})
        found = await NetworkLinkCollector(node).collect()
        assert "vethd1f7ef2" not in {r.ident for r in found}

    async def test_non_json_output_raises_rather_than_reading_as_clean(self) -> None:
        node = FakeNode(
            "n1",
            {("ip", "-json", "link", "show"): 'Option "-json" is unknown\n'},
        )
        with pytest.raises(CommandFailed, match="did not return JSON"):
            await NetworkLinkCollector(node).collect()

    async def test_details_is_never_requested(self) -> None:
        """`ip -details -json` is unparseable wherever a vxlan device exists — see the
        `IP_LINK_DETAILS_BROKEN` capture. Passing `-details` would make the collector raise on
        exactly the hosts this suite exists for."""
        node = FakeNode("n1", {("ip", "-json", "link", "show"): IP_LINK_JSON})
        await NetworkLinkCollector(node).collect()
        assert node.calls == [("ip", "-json", "link", "show")]

    async def test_the_iproute2_fanmap_output_would_be_rejected(self) -> None:
        node = FakeNode("n1", {("ip", "-json", "link", "show"): IP_LINK_DETAILS_BROKEN})
        with pytest.raises(CommandFailed, match="did not return JSON"):
            await NetworkLinkCollector(node).collect()

    async def test_state_change_alone_is_not_a_different_resource(self) -> None:
        """`detail` carries operstate, so a bridge going UP must not read as a new device."""
        down = Resource("link", "n1", "baibr4098", "bridge mtu=1450 state=DOWN")
        up = Resource("link", "n1", "baibr4098", "bridge mtu=1450 state=UP")
        assert down == up
        assert {down} - {up} == set()


