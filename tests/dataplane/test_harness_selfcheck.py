class TestProcessGauges:
    def test_parses_one_line_per_process(self) -> None:
        collector = ProcessGaugeCollector(FakeNode("n1", {}), pattern="ai.backend.agent")
        gauges = collector.parse("4242\tpython -m ai.backend.agent\t137\t9\t512000\n")
        assert gauges[4242].fds == 137
        assert gauges[4242].rss_kib == 512000

    def test_our_own_pgrep_shell_is_not_a_gauge(self) -> None:
        """`pgrep -f <pattern>` also matches the shell running it, and that PID changes on every
        poll — enough churn to hide real drift behind a set that never repeats."""
        collector = ProcessGaugeCollector(FakeNode("n1", {}), pattern="ai.backend.agent")
        gauges = collector.parse(
            "4242\tpython -m ai.backend.agent.server\t137\t9\t512000\n"
            "9001\tsh -c pgrep -f 'ai.backend.agent' | while read -r p\t3\t1\t1520\n"
            "9002\tsudo -n sh -c pgrep -f 'ai.backend.agent'\t8\t1\t5848\n"
        )
        assert sorted(gauges) == [4242]

    def test_a_row_split_across_lines_is_rejected(self) -> None:
        """A command line containing a newline used to spill one row onto two. Failing loudly
        beats silently dropping the process whose fd count is the thing being watched."""
        collector = ProcessGaugeCollector(FakeNode("n1", {}), pattern="ai.backend.agent")
        with pytest.raises(CommandFailed, match="unexpected gauge line"):
            collector.parse("4242\t/usr/bin/zsh -c source /home/x/snapshot\n")

    def test_fd_growth_beyond_the_allowance_fails(self) -> None:
        before = {1: ProcessGauge("n1", 1, "agent", 100, 8, 1000)}
        after = {1: ProcessGauge("n1", 1, "agent", 400, 8, 1000)}
        with pytest.raises(AssertionError, match="fds 100 -> 400"):
            assert_no_drift(before, after)

    def test_a_restarted_process_is_not_compared(self) -> None:
        before = {1: ProcessGauge("n1", 1, "agent", 100, 8, 1000)}
        after = {2: ProcessGauge("n1", 2, "agent", 9999, 99, 9_000_000)}
        assert_no_drift(before, after)


