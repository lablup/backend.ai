class TestScratchDirCollector:
    SCRATCH_ROOT = "/srv/scratches"
    PROBE = (
        "sh",
        "-c",
        f'for d in {SCRATCH_ROOT}; do if [ -d "$d" ]; then printf "%s\\n" "$d"; fi; done',
    )
    LIST = (
        "sh",
        "-c",
        f'for d in {SCRATCH_ROOT}; do find "$d" -mindepth 1 -maxdepth 1 -type d; done',
    )

    def _node(self, *, root_exists: bool, listing: str) -> FakeNode:
        return FakeNode(
            "n1",
            {
                self.PROBE: f"{self.SCRATCH_ROOT}\n" if root_exists else "",
                self.LIST: listing,
            },
        )

    async def _collect(self, node: FakeNode, live: set[str]) -> set[Resource]:
        async def live_ids() -> set[str]:
            return live

        return await ScratchDirCollector(
            node, roots=(self.SCRATCH_ROOT,), live_ids=live_ids
        ).collect()

    async def test_a_scratch_whose_kernel_is_gone_is_reported(self) -> None:
        node = self._node(
            root_exists=True,
            listing=f"{self.SCRATCH_ROOT}/live-kernel\n{self.SCRATCH_ROOT}/783ad6cc\n",
        )
        found = await self._collect(node, {"live-kernel"})
        assert {r.ident for r in found} == {f"{self.SCRATCH_ROOT}/783ad6cc"}

    async def test_a_scratch_with_a_live_container_record_is_not_a_leak(self) -> None:
        node = self._node(root_exists=True, listing=f"{self.SCRATCH_ROOT}/live-kernel\n")
        assert await self._collect(node, {"live-kernel"}) == set()

    async def test_a_missing_root_raises_instead_of_reporting_clean(self) -> None:
        """A scratch-root that does not exist means the harness was pointed at the wrong
        directory. Returning an empty set would certify every host as clean."""
        node = self._node(root_exists=False, listing="")
        with pytest.raises(CommandFailed, match="none of the configured scratch roots exist"):
            await self._collect(node, set())


