class FakeNode:
    """Replays canned output for exact argv matches, and fails loudly on anything else.

    An unmatched command raises rather than returning empty output: a collector that quietly asked
    the wrong question would otherwise pass its own test.
    """

    _name: str
    _responses: dict[tuple[str, ...], str]
    calls: list[tuple[str, ...]]

    def __init__(self, name: str, responses: dict[tuple[str, ...], str]) -> None:
        self._name = name
        self._responses = responses
        self.calls = []

    @property
    def name(self) -> str:
        return self._name

    async def run(self, argv: list[str], *, check: bool = True) -> CommandResult:
        key = tuple(argv)
        self.calls.append(key)
        if key not in self._responses:
            raise CommandFailed(f"[{self._name}] unstubbed command: {argv}")
        return CommandResult(self._name, key, 0, self._responses[key], "")


